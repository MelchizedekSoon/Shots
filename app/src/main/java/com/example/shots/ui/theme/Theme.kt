@Composable
fun ShotsTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic color is available on Android 12+
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
//        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
//            val context = LocalContext.current
//            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
//        }
        darkTheme -> DarkColor
        else -> LightColor
    }
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            (view.context as Activity).window.statusBarColor = colorScheme.primary.toArgb()
            ViewCompat.getWindowInsetsController(view)?.isAppearanceLightStatusBars = darkTheme
        }
        //This one I added on March 20th, 2024 - I don't know if i'll keep it
        //but it's to make the status bar icons and text black
        SideEffect {
            val activity = (view.context as Activity)
            val window = activity.window

            val colorWhite = ContextCompat.getColor(view.context, R.color.white)

            val isLight = colorScheme != DarkColor
//            (view.context as Activity).window.statusBarColor = colorScheme.primary.toArgb()
            (view.context as Activity).window.statusBarColor = colorWhite
            ViewCompat.getWindowInsetsController(view)?.isAppearanceLightStatusBars = darkTheme
            WindowInsetsControllerCompat(window, window.decorView).isAppearanceLightStatusBars =
                isLight


//            // Set the status bar color
//            WindowCompat.setDecorFitsSystemWindows(window, false)
//            WindowInsetsControllerCompat(window, view.rootView).apply {
//                isAppearanceLightStatusBars = !darkTheme
//                systemBarsAppearance = WindowInsetsControllerCompat.APPEARANCE_LIGHT_STATUS_BARS
//                setSystemBarsColor(colorScheme.primary.toArgb())
//            }
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}